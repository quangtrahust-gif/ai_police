def in_thong_tin(ten, tuoi=18, lop="CNTT"):
    print(f"Tên: {ten}, Tuổi: {tuoi}, Lớp: {lop}")

in_thong_tin("An")                 # Tên: An, Tuổi: 18, Lớp: CNTT
in_thong_tin("Bình", 20)           # Tên: Bình, Tuổi: 20, Lớp: CNTT
in_thong_tin("Chi", 22, "KHDL")    # Tên: Chi, Tuổi: 22, Lớp: KHDL
